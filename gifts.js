// JavaScript to change the image on hover
const imageContainer1 = document.getElementById('imageContainer1');
const image1 = document.getElementById('image1');

// Change the image source when hovering over it
imageContainer1.addEventListener('mouseover', () => {
    image1.src = "https://flowerdose.com.au/cdn/shop/products/Flowerdose_Gifting-66.jpg?v=1679825939&width=1780";
});

// Restore the original image on mouseout
imageContainer1.addEventListener('mouseout', () => {
    image1.src = "https://flowerdose.com.au/cdn/shop/products/Flowerdose_Gifting-59.jpg?v=1679825830&width=1780";
});




